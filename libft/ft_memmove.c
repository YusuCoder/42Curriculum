#include "libft.h"

void	*ft_memmove(void *to, const void *from, size_t len)
{
	char	*d;
	char	*s;

	if (to == NULL && from == NULL)
	{
		return (NULL);
	}
	d = (char *)to;
	s = (char *)from;
	if (d < s)
	{
		while (len--)
			*d++ = *s++;
	}
	else
	{
		d += len - 1;
		s += len - 1;
		while (len--)
			*d-- = *s--;
	}
	return (to);
}
