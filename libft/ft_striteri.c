#include "libft.h"

void	ft_striteri(char *s1, void (*f)(unsigned int, char *))
{
	int	i;

	i = 0;
	if (!s1 || !f)
	{
		return ;
	}
	while (s1[i])
	{
		f(i, s1 + i);
		i++;
	}
	s1[i] = '\0';
}
